/* HTML 이스케이프 */
function escHTML(s) {
  return String(s == null ? "" : s)
    .replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;").replace(/'/g, "&#39;");
}

/* HTML 속성값 이스케이프 */
function escAttr(s) {
  return String(s == null ? "" : s)
    .replace(/&/g, "&amp;").replace(/"/g, "&quot;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
}

/* splitDefBold 의 HTML 버전 — 따옴표 안 정의 단어를 <strong> 으로 감싸 반환 */
function splitDefBoldHTML(text) {
  if (!text) return "";
  const PATTERNS = [
    /(?:이하\s*)?["“]([^"”]{1,40})["”]\s*이?라(?:고)?\s*(?:한다|함|하고)/g,
    /["“]([^"”]{1,40})["”]\s*(?:이?란|은|는)(?![가-힣])/g
  ];
  const ranges = [];
  for (const re of PATTERNS) {
    re.lastIndex = 0;
    let m;
    while ((m = re.exec(text)) !== null) {
      const inner = m[1];
      const innerStart = text.indexOf(inner, m.index);
      if (innerStart < 0) continue;
      ranges.push({ start: innerStart, end: innerStart + inner.length });
    }
  }
  if (!ranges.length) return escHTML(text);
  ranges.sort((a, b) => a.start - b.start);
  const merged = [];
  for (const r of ranges) {
    const last = merged[merged.length - 1];
    if (last && r.start <= last.end) last.end = Math.max(last.end, r.end);
    else merged.push({ start: r.start, end: r.end });
  }
  let html = "";
  let cur = 0;
  for (const r of merged) {
    if (r.start > cur) html += escHTML(text.slice(cur, r.start));
    html += "<strong>" + escHTML(text.slice(r.start, r.end)) + "</strong>";
    cur = r.end;
  }
  if (cur < text.length) html += escHTML(text.slice(cur));
  return html;
}

