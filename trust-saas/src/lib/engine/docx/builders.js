/* 한글 금액 변환 — 위변조 방지를 위해 일십·일백·일천 표기 (PDF 표준안) */
function amountToHangul(amount) {
  if (amount == null || amount === "") return "";
  let n = parseInt(String(amount).replace(/[^\d-]/g, ""), 10);
  if (!Number.isFinite(n) || n === 0) return "영원정";
  const digits = ["", "일", "이", "삼", "사", "오", "육", "칠", "팔", "구"];
  const positions4 = ["", "십", "백", "천"];
  const positions = ["", "만", "억", "조", "경"];
  let groups = [];
  while (n > 0) { groups.push(n % 10000); n = Math.floor(n / 10000); }
  let out = "";
  for (let i = groups.length - 1; i >= 0; i--) {
    const g = groups[i];
    if (g === 0) continue;
    let s = "";
    const gs = String(g);
    for (let j = 0; j < gs.length; j++) {
      const d = parseInt(gs[j], 10);
      const pos = gs.length - 1 - j;
      // 모든 자리 "일"을 명시 (PDF 표준안)
      if (d > 0) s += digits[d] + positions4[pos];
    }
    out += s + positions[i];
  }
  return out + "원정";
}

