/* 본문 제1~31조 — docx.js 요소 빌더 (4단계 통합 출력에서 사용) */
function buildBodyChildren() {
  const { Paragraph, TextRun, AlignmentType, HeadingLevel } = docx;
  const out = [];

  // 페이지2 최상단 — '부동산담보신탁계약서' (bold, 11pt = size 22, 가운데)
  out.push(new Paragraph({
    alignment: AlignmentType.CENTER, spacing: { after: 200 },
    children: [new TextRun({ text: "부동산담보신탁계약서", bold: true, size: 22 })]
  }));

  // 신탁본문 — Heading 1 (Word 탐색창 최상위 항목)
  out.push(new Paragraph({
    heading: HeadingLevel.HEADING_1,
    alignment: AlignmentType.CENTER, spacing: { after: 280 },
    children: [new TextRun({ text: "신탁본문", bold: true, size: 22 })]
  }));

  // 전문(前文) — 위탁자명 부분만 하이라이트 + after 절의 정의(이하 "위탁자/수탁자") BOLD
  const names = state.form.trustors.map(t => (t.name || "").trim()).filter(Boolean);
  const trustorJoined = names.length ? names.join(", ") : "[위탁자]";
  const splitIdx = CONTRACT_PREAMBLE.indexOf("{trustors}");
  const before = CONTRACT_PREAMBLE.slice(0, splitIdx);
  const after = CONTRACT_PREAMBLE.slice(splitIdx + "{trustors}".length);
  out.push(new Paragraph({
    spacing: { after: 220 }, alignment: AlignmentType.JUSTIFIED,
    children: [
      new TextRun({ text: before, size: 20 }),
      new TextRun({ text: trustorJoined, size: 20, shading: hlShading() }),
      ...splitDefBold(after, { size: 20 })
    ]
  }));

  CONTRACT_BODY.forEach(art => {
    // 조 제목 — Heading 2 (Word 탐색창 2단계 항목)
    out.push(new Paragraph({
      heading: HeadingLevel.HEADING_2,
      spacing: { before: 200, after: 80 },
      children: [new TextRun({ text: `제${art.n}조 (${art.t})`, bold: true, size: 20 })]
    }));

    const hangCount = art.c.filter(x => x.l === 0 && !x.p).length;
    const numberHang = hangCount >= 2;
    let hangIdx = 0, hoIdx = 0, mokIdx = 0;

    art.c.forEach(line => {
      if (line.l === 0) {
        hoIdx = 0; mokIdx = 0;
        let prefix = "";
        if (!line.p && numberHang) { prefix = CIRCLED_NUMS[hangIdx] + " "; hangIdx++; }
        out.push(new Paragraph({
          spacing: { after: 60 }, alignment: AlignmentType.JUSTIFIED,
          indent: line.p ? undefined : { left: INDENT_HANG },
          children: splitDefBold(prefix + line.t, { size: 20 })
        }));
      } else if (line.l === 1) {
        mokIdx = 0; hoIdx++;
        out.push(new Paragraph({
          spacing: { after: 50 }, alignment: AlignmentType.JUSTIFIED,
          indent: { left: INDENT_HO },
          children: splitDefBold(`${hoIdx}. ${line.t}`, { size: 20 })
        }));
      } else if (line.l === 2) {
        out.push(new Paragraph({
          spacing: { after: 50 }, alignment: AlignmentType.JUSTIFIED,
          indent: { left: INDENT_MOK },
          children: splitDefBold(`${HO_HANGUL[mokIdx]}. ${line.t}`, { size: 20 })
        }));
        mokIdx++;
      }
    });
  });

  return out;
}

