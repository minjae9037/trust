async function generateJointDocx() {
  if (!docx) { alert("docx 엔진이 로드되지 않았습니다."); return; }
  const { Document, Packer, Footer, Paragraph, TextRun, PageNumber, AlignmentType } = docx;
  let blob;
  try {
    const children = buildJointDocxChildren();
    const doc = new Document({
      styles: { default: { document: {
        run: { font: "맑은 고딕", size: 20 },
        paragraph: { spacing: { line: 276, lineRule: "auto" } }
      } } },
      sections: [{
        properties: { page: {
          size: { width: 11906, height: 16838 },
          margin: { top: 1440, right: 1440, bottom: 1440, left: 1440 }
        } },
        footers: { default: new Footer({ children: [new Paragraph({
          alignment: AlignmentType.CENTER,
          children: [
            new TextRun({ children: [PageNumber.CURRENT], size: 18 }),
            new TextRun({ text: " / ", size: 18 }),
            new TextRun({ children: [PageNumber.TOTAL_PAGES], size: 18 })
          ]
        })] }) },
        children
      }]
    });
    const raw = await Packer.toBlob(doc);
    blob = new Blob([raw], { type: "application/vnd.openxmlformats-officedocument.wordprocessingml.document" });
  } catch (e) {
    alert("docx 생성 중 오류: " + (e.message || e));
    console.error(e);
    return;
  }
  const gName = (state.jointForm.gap.name || "갑").trim();
  const fname = `공동사업표준협약서_${gName}.docx`;
  if (window.navigator && typeof window.navigator.msSaveOrOpenBlob === "function") {
    window.navigator.msSaveOrOpenBlob(blob, fname);
    return;
  }
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url; a.download = fname; a.rel = "noopener";
  document.body.appendChild(a); a.click(); document.body.removeChild(a);
  setTimeout(() => URL.revokeObjectURL(url), 1500);
}

